package me.skrilled.ui.clickui;

public class pageNumBar {
    public float x1;
    public float y1;
    public float x2;
    public float y2;
    public int num = 1;

    public pageNumBar(float x1, float y1, float x2, float y2, int num) {
        this.x1 = x1;
        this.y1 = y1;
        this.x2 = x2;
        this.y2 = y2;
        this.num = num;
    }
}
